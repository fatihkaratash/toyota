package com.toyota.market.trend;

/**
 * Represents different market trend modes that can be simulated.
 */
public enum MarketTrendMode {
    /**
     * Upward trending market (prices tend to rise)
     */
    BULL,
    
    /**
     * No specific trend direction (default behavior)
     */
    NEUTRAL,
    
    /**
     * Downward trending market (prices tend to fall)
     */
    BEAR
}
